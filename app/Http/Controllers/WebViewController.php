    public function menu()
    {
        // Mengambil semua data produk dari database
        $products = Product::paginate(9);
    
        // Mengambil kategori unik dari database
        $categories = Product::select('category')
            ->distinct()
            ->pluck('category');
    
        // Mengirim data produk dan kategori ke view menu
        return view('menu', compact('products', 'categories'));
    }public function promo()
    {
        // Ambil semua kategori yang memiliki produk promo aktif
        $categories = Product::where('is_promo', true)
                ->where('status', 'active')
                ->select('category')
                ->distinct()
                ->get()
                ->pluck('category');
        
        $categoryPromos = [];
        
        foreach ($categories as $category) {
            // Ambil produk promo untuk kategori ini
            $products = Product::where('is_promo', true)
                        ->where('status', 'active')
                        ->where('category', $category)
                        ->orderBy('promo_order')
                        ->take(10)
                        ->get();
            
            // Tambahkan ke hasil jika ada produk
            if ($products->count() > 0) {
                $categoryPromos[] = [
                    'category' => $category,
                    'products' => $products
                ];
            }
        }
        
        // Ambil semua tipe promo yang aktif
        $promoTypes = Product::where('is_promo', true)
                    ->where('status', 'active')
                    ->whereNotNull('promo_type')
                    ->select('promo_type', 'promo_name')
                    ->distinct()
                    ->get()
                    ->groupBy('promo_type');
        
        $typePromos = [];
        
        foreach ($promoTypes as $type => $promos) {
            // Gunakan nama promo pertama sebagai nama tampilan
            $displayName = $promos->first()->promo_name ?: 'Promo ' . ucfirst($type);
            
            // Ambil produk untuk tipe promo ini
            $products = Product::where('is_promo', true)
                        ->where('status', 'active')
                        ->where('promo_type', $type)
                        ->orderBy('promo_order')
                        ->take(10)
                        ->get();
            
            // Tambahkan ke hasil jika ada produk
            if ($products->count() > 0) {
                $typePromos[] = [
                    'promo_type' => $type,
                    'display_name' => $displayName,
                    'products' => $products
                ];
            }
        }
        
        // Ambil semua banner promo
        $promoProducts = Product::where('is_promo', true)
                        ->where('status', 'active')
                        ->whereNotNull('promo_banner')
                        ->get();
        
        return view('promo', compact('categoryPromos', 'typePromos', 'promoProducts'));
    }
